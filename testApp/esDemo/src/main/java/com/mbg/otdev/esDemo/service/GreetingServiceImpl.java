package com.mbg.otdev.esDemo.service;

import com.google.common.collect.Lists;
import com.mbg.otdev.esDemo.dao.GreetingRepository;
import com.mbg.otdev.esDemo.model.Greeting;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import java.util.List;
import java.util.Optional;

@Service
public class GreetingServiceImpl implements GreetingService{
    @Autowired
    private GreetingRepository repository;

    @Override
    public List<Greeting> getAll() {
        return Lists.newArrayList(repository.findAll());
    }

    @Override
    public Optional<Greeting> findOne(String id) {
        return repository.findById(id);
    }


    @Override
    public Greeting create(Greeting greeting) {
        return repository.save(greeting);
    }

    @Override
    public Greeting update(Greeting greeting) {
        Optional<Greeting> persitedGreeting = repository.findById(greeting.getId());
        if( !persitedGreeting.isPresent() ) {
            return null;
        }

        return repository.save(greeting);
    }

    @Override
    public List<Greeting> getGreetingByUsername(String username) {
        return repository.findByUsername(username);
    }

    @Override
    public void delete(String id) {
        repository.deleteById(id);
    }

}
